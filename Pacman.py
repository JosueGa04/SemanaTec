# Selecciona aleatoriamente uno de los tableros definidos anteriormente.
tiles = choice(boards)

def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20 # Convierte la coordenada x al índice correspondiente.
    y = (180 - floor(point.y, 20)) / 20 # Convierte la coordenada y al índice correspondiente.
    index = int(x + y * 20) # Calcula el índice lineal basado en x e y.
    return index

def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    # Verifica si las coordenadas del punto están alineadas con la cuadrícula.
    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Draw world using path."""
    # Establece el fondo de la ventana en negro.
    bgcolor('black')
    # Establece el color del trazado en azul.
    path.color('blue')

    # Recorre todos los índices del tablero.
    for index in range(len(tiles)):
        tile = tiles[index]

        # Solo dibuja si el tile es un camino (1).
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            # Si es un camino, dibuja un punto blanco en el centro.
            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    """Move pacman and all ghosts."""
    # Deshace el texto anterior del marcador.
    writer.undo()
    # Actualiza y muestra la puntuación en pantalla.
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    # Si la posición de Pac-Man coincide con una baldosa con puntos, los elimina y suma puntos.
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    # Dibuja a Pac-Man en su nueva posición como un punto amarillo.
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')


    # Mueve a cada fantasma.
    for point, course in ghosts:
        # Calcula la nueva posición del fantasma sumando su curso (course) a su posición actual.
        if valid(point + course):
            point.move(course)
        else:
     	    # Si el siguiente movimiento no es válido, el fantasma cambia de dirección aleatoriamente.
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        # Dibuja a cada fantasma en su nueva posición como un punto rojo.
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Verifica si algún fantasma ha alcanzado a Pac-Man. Si es así, termina el juego.
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    #  Establece el temporizador para volver a ejecutar la función 'move' después de 100 ms.
    ontimer(move, 100)

def change(x, y):
    """Change pacman aim if valid."""
    # Solo cambia la dirección si la nueva es válida.
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

# Configura las teclas de dirección para controlar a Pac-Man.
setup(420, 420, 370, 0)
hideturtle()
tracer(False)
writer.goto(160, 160)
writer.color('white')
writer.write(state['score'])
listen()
onkey(lambda: change(5, 0), 'Right')
onkey(lambda: change(-5, 0), 'Left')
onkey(lambda: change(0, 5), 'Up')
onkey(lambda: change(0, -5), 'Down')
world()
move()
done()

